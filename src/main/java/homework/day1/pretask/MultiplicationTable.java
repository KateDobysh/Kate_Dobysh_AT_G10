package main.java.homework.day1.pretask;

public class MultiplicationTable {
    public static void main(String[] args) {
        int i = 1;
        int b = 5;

        for (i = 1; i <= 10; i++) {
            int result = i * b;
            System.out.println(i + " x " + b + " = " + result);
        }
    }
}
